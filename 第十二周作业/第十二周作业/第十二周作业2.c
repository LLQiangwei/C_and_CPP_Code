//题目内容：有若干个学生的成绩（每个学生有4门课程），要求在用户输入学生序号以后，能输出该学生的全部成绩。用指向数组的指针变量实现。
//使用数组 float score[][4]={{60,70,80,90},{56,89,67,88},{34,78,90,66}}
//输入格式:%d
//输出格式：%8.2f
//输入样例：1
//输出样例：
//   56.00   89.00   67.00   88.00
//
#include<stdio.h>

int main()
{
	float score[][4]={{60,70,80,90},{56,89,67,88},{34,78,90,66}};
	int n = 0,i = 0;
	float *ps;

	scanf("%d",&n);
	ps = &score[n][0];

	for(i = 0;i < 4;i++)
	{
		printf("%8.2f",*ps);
		ps++;
	}

	return 0;
}